def addition(num1, num2):
    result = num1 + num2
    print(f'{num1} + {num2} is equal to {result}')
    return result

def subtraction(num1, num2):
    result = num1 - num2
    print(f'{num1} - {num2} is equal to {result}')
    return result

def multiplication(num1, num2):
    result = num1 * num2
    print(f'{num1} * {num2} is equal to {result}')
    return result

def division(num1, num2):
    result = num1 / num2
    print(f'{num1} / {num2} is equal to {result}')
    return result

def power(num1, num2):
    result = num1 ^ num2
    print(f'{num1} ^ {num2} is equal to {result}')
    return result

def modulo(num1, num2):
    result = num1 % num2
    print(f'{num1} % {num2} is equal to {result}')
    return result