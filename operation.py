def add(num_1, num_2):
    result = int(num_1) + int(num_2)
    print(f'{num_1} + {num_2} is equal to {result}')
    return result

def resta(num_1, num_2):
    result = int(num_1) - int(num_2)
    print(f'{num_1} - {num_2} is equal to {result}')
    return result

def multiplicacion(num_1, num_2):
    result = int(num_1) * int(num_2)
    print(f'{num_1} * {num_2} is equal to {result}')
    return result
